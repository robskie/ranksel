// select1_64 returns the bit
// position of the ith set bit.
func select1_64(v uint64, i int) int {
	s := v
	ii := uint64(i) - 1

	s -= (s & 0xAAAAAAAAAAAAAAAA) >> 1
	s = (s & 0x3333333333333333) + ((s >> 2) & 0x3333333333333333)
	s = (s + (s >> 4)) & 0x0F0F0F0F0F0F0F0F
	s *= 0x0101010101010101

	t := ii * 0x0101010101010101
	u := ((t | 0x8080808080808080) - (s & 0x7F7F7F7F7F7F7F7F)) ^ s ^ t
	u = (u & 0x8080808080808080) >> 7
	u = (u * 0x0101010101010101 >> 53) & 0xFFFFFFFFFFFFFFF8

	t = ii - (((s << 8) >> u) & 0xFF)
	return int(u) + int(selectTable[((v>>u)&0xFF)|(t<<8)])
}

// rank1_64 returns the number of 1s from index
// 0 to i starting from the least significant bit.
func rank1_64(v uint64, i int) int {
	return bit.PopCount(v << uint(63-i))
}
